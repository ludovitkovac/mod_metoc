function showHideHidi() {
    if (document.getElementById('hididiv').style.display === "none") {
        document.getElementById('hididiv').style.display = "inline";
    } else if (document.getElementById('hididiv').style.display === "inline") {
        document.getElementById('hididiv').style.display = "none";
    }
}

